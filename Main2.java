public class Main2
{
    public static void main(String[] args) {
        int a=20;
        int b=5;
        System.out.println("Use of Operator in Java");
        int sum = a+b;
        System.out.println("Addition of the number is : "+sum);
        int diff = a-b;
        System.out.println("Subtraction of the number is : "+diff);
        int pro = a*b;
        System.out.println("Multiplication of the number is : "+pro);
        int div = a/b;
        System.out.println("Division of the Number is : "+div);
        int rem = a%b;
        System.out.println("Remainder of the number is : "+rem);
        
   }
}
